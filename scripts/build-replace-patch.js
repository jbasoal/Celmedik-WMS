const patch = {};
const stats = { delBio: 0, delLA: 0, newBio: 0, newLA: 0, udBio: 0, udLA: 0, missingSku: new Set() };

// 1) Borrar movimientos existentes en BIODIAL y LOS ANGELES
for (const [id, m] of Object.entries(movsExist)) {
  if (m.bodega === BIODIAL) { patch[`movimientos/${id}`] = null; stats.delBio++; }
  else if (m.bodega === LOSANG) { patch[`movimientos/${id}`] = null; stats.delLA++; }
}

// 2) Construir nuevas entradas
function buildEntrada(bodega, ubic, sku, loteRaw, qty, venceRaw, idx, bagStat) {
  const prod = prodsExist[sku];
  if (!prod) { stats.missingSku.add(sku); return null; }
  return {
    tipo: 'Entrada',
    sku,
    lote: normLote(loteRaw),
    descripcion: prod.descripcion || '',
    tipoProducto: prod.tipo || '',
    cantidad: qty,
    vence: parseFecha(venceRaw),
    precio: 0,
    status: 'Aprobado',
    bodega,
    ubicacion: ubic,
    proveedor: prod.fabricante || '',
    referencia: '',
    observaciones: OBS,
    fecha: FECHA_ISO,
    usuario: USUARIO,
    documentoId: DOC_ID,
    fechaDocumento: FECHA_DOC
  };
}

const ts0 = Date.now();
let i = 0;
for (const f of FILAS_BIODIAL) {
  const [ubic, sku, loteRaw, qty, venceRaw] = f;
  const mov = buildEntrada(BIODIAL, ubic, sku, loteRaw, qty, venceRaw, i, 'bio');
  if (!mov) continue;
  patch[`movimientos/${ts0 + i}`] = mov;
  stats.newBio++; stats.udBio += qty; i++;
}
for (const f of FILAS_LOSANG) {
  const [ubic, sku, loteRaw, qty, venceRaw] = f;
  const mov = buildEntrada(LOSANG, ubic, sku, loteRaw, qty, venceRaw, i, 'la');
  if (!mov) continue;
  patch[`movimientos/${ts0 + i}`] = mov;
  stats.newLA++; stats.udLA += qty; i++;
}

// 3) Auditoria
patch[`auditoria/${ts0 + 1000000}`] = {
  accion: 'Reemplazo masivo Entradas',
  modulo: 'Movimientos',
  documentoId: DOC_ID,
  detalle: `Borrados: ${stats.delBio} (Biodial) + ${stats.delLA} (Los Angeles). Nuevas Entradas: ${stats.newBio} Biodial (${stats.udBio} ud) + ${stats.newLA} Los Angeles (${stats.udLA} ud).`,
  fecha: FECHA_ISO,
  usuario: USUARIO
};

fs.writeFileSync('scripts/_replace-patch.json', JSON.stringify(patch, null, 2));

