function parseFecha(s) {
  if (!s) return '';
  const t = String(s).trim().toLowerCase();
  if (t === 'n/a' || t === 'na' || !t) return '';
  const m = t.match(/^(\d{1,2})-(\d{1,2})-(\d{4})$/);
  if (!m) return '';
  return `${m[3]}-${m[2].padStart(2,'0')}-${m[1].padStart(2,'0')}`;
}
function normLote(s) {
  if (!s) return 'S/N';
  const t = String(s).trim();
  return (/^n\/a$/i.test(t) || !t) ? 'S/N' : t;
}

